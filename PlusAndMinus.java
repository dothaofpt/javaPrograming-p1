// Cộng và trừ trong java
public class PlusAndMinus {
    public static void main(String[] args) {
        int zebrasInZoo = 8;
        int giraffesInZoo = 6;
        int animalInZoo = giraffesInZoo + zebrasInZoo;
        System.out.println(animalInZoo);
        zebrasInZoo--;
        zebrasInZoo--;
        System.out.println(zebrasInZoo);

    }
}
